							<p>Det gjenstår fortsatt en del kvalitetssikring og registering. Noen data vil bli overført og registrert av oss i HR, mens andre data trenger vi din hjelp for å få registrert.</p>
							<p>Dette gjelder i hovedsak informasjon knyttet til person- og familieopplysninger i Personalportalen og ferie-, fleksi og kompsaldo i Tidbank.</p>
							<p>Vi henstiller følgelig alle våre ansatte om hjelp til å kvalitetssikre dataene som ligger inne i de ulike systemene.</p>
							<h3>Personalportalen (HR systemet)</h3>
							<p><strong>Person og familieopplysninger.</strong>  Informasjonen som er knyttet til deg som person kan være mangelfull i Personalportalen bl.a mangler vi informasjon om nærmeste pårørende og øvrig familie. Dette er viktig både fordi vi trenger pårørendeinformasjon ved ulykker og andre uforutsette hendelser, men også fordi alle rettigheter til egen og egne barns sykdom defineres ut i fra hvilken informasjon som til enhver tid ligger i systemet. Vi henstiller derfor alle våre nye ansatte om å kvalitetssikre persondataene som ligger i lønnssystemet og med det fylle ut skjemaet Person og familieinformasjon som ligger under Skjema/ Nytt skjema/ Person og familieinformasjon i Personalportalen. For mer utfyllende informasjon vennligst se Rutinestøtte samme sted.   </p>
							<p>Arbeidsforhold, lønn og lønnsslipper. Lønnen for januar 2016 er utbetalt og lønnslippen er nå tilgjengelig i Personalportalen. Fint om alle kan gå inn i Personalportalen og kvalitetssikre om lønnen og lønnsslippen er som forventet. Det viktigste er en kvalitetskontroll av</p>
							<ul>
								<li>Kontonummer</li>
								<li>Lønnstrinn iht. gjeldende arbeidsavtale</li>
								<li>Stillingsprosent iht. gjeldende arbeidsavtale</li>
								<li>Skatt</li>
								<li>Variable tillegg (b-tillegg etc.)</li>
								<li>Variable trekk (fagforening etc.)</li>
							</ul>
