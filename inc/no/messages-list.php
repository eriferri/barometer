							<h3>Tidbank - ferie/fleksisaldo/kompsaldo</h3>
							<p>Ved instituttet er det i Tidbank man til enhver tid finner saldoen for ferie, fleksi -og kompsaldo. Det er avtalt at saldoen pr 31.12.15 overføres i sin helhet. Vi mangler fortsatt en del opplysninger i denne sammenhengen, men vi i HR vil oppdatere systemet fortløpende så fort vi får informasjonen vi trenger. Målet er at informasjonen for alle ansatte skal være kvalitetssikret og registrert inn i Tidbank innen 1.mars 2016 (som for resten av instituttet.)</p>
							<p>Saldoene vil til enhver tid gjenspeile hva som er registrert i systemet så uansett er det viktig at alle registreringer i Tidbank gjøres fortløpende av den ansatte. Minustimene som følge av manglende registreringer i romjulen er selvfølgelig en feil fra vår side og vil bli fjernet av vår leverandør snarest.  </p>
							<p>Det har kommet noen spørsmål i etterkant av introduksjonen/opplæringen av FHI sine interne systemer slik at det er behov for noen presiseringer. Man søker ikke om ferie eller avspaseringer i noen av de elektroniske systemene, men man avtaler dette direkte med leder.</p>
							<ul>
								<li>Ferie med lønn: avtales med nærmeste leder og registreres deretter i Tidbank</li>
								<li>Ferie uten lønn: avtales med nærmeste leder, for trekk av lønn send mail til HR/lønn ved Kjetil Andersen, dagene registreres deretter i Tidbank</li>
								<li>Avspasering: avtales med nærmeste leder og registreres i Tidbank med kode «Avsp fleks»</li>
								<li>For mer utfyllende informasjon se informasjon på Agora om Arbeidstid /arbeidsordninger og Ferie.</li>
							</ul>
